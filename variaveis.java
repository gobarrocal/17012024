public class variaveis {
    public static void main(String[] args) {
        //...
// Sintaxe: tipoDeDado nomeDaVariavel;
    int idade;             // Declaração de uma variável do tipo inteiro chamada idade
    double altura;         // Declaração de uma variável do tipo double chamada altura
    char letra;            // Declaração de uma variável do tipo char chamada letra
    boolean status;        // Declaração de uma variável do tipo boolean chamada status
    String nome;           // Declaração de uma variável do tipo String chamada nome

    idade = 25;           // Inicialização da variável idade com o valor 25
    altura = 1.75;        // Inicialização da variável altura com o valor 1.75
    letra = 'A';          // Inicialização da variável letra com o caractere 'A'
    status = true;        // Inicialização da variável status com o valor verdadeiro
    nome = "João";        // Inicialização da variável nome com a string "João"
        
    System.out.println("Idade: " + idade); 
    System.out.println("Altura: " + altura);
    }
}